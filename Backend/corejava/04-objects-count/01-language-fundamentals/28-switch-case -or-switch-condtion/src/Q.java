class Q
{
	public static void main(String[] args) 
	{
		short s = 10;
		switch(s)
		{
			case 10:
				System.out.println("from case 10");
				break;
			default:
				System.out.println("from case default");
				break;
			case 20:
				System.out.println("from case 20");
				break;
			case 30:
				System.out.println("from case 30");
		}
	}
}